public class Client {
    private String name;
    private String id;
    private String telephone;
    
    public String getName(){
        return name;
    }
    public void setName(String name){
        this.name = name;
    }
    public String getId(){
        return id;
    }
    public void setId(String id){
        this.id = id;
    }
    public String getTelephone(){
        return telephone;
    }
    public void setTelehone(String telephone){
        this.telephone = telephone;
    }
}
